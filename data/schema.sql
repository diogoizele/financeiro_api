DROP TABLE IF EXISTS activity CASCADE;
DROP TABLE IF EXISTS purchase CASCADE;
DROP TABLE IF EXISTS person CASCADE;

CREATE TABLE person (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    password VARCHAR(128) NOT NULL,
    wage NUMERIC(8,2) NOT NULL
);
ALTER TABLE person ADD CONSTRAINT pk_user PRIMARY KEY (id);

CREATE TABLE activity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    title VARCHAR(512) NOT NULL,
    date TIMESTAMP NOT NULL,
    amount NUMERIC(8, 2) NOT NULL,
    description TEXT,
    category VARCHAR(128) NOT NULL,

    person_id BIGINT NOT NULL
);
ALTER TABLE activity ADD CONSTRAINT pk_activity PRIMARY KEY (id);
ALTER TABLE activity ADD CONSTRAINT fk_activity_person FOREIGN KEY (person_id) REFERENCES person;
ALTER TABLE activity ADD CONSTRAINT ck_activity_category CHECK (category IN ('EXPENSE', 'INCOME', 'CONTRIBUTION'));

CREATE TABLE purchase (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    item VARCHAR(512) NOT NULL,
    price NUMERIC(10, 2) NOT NULL,
    description TEXT,

    person_id BIGINT NOT NULL
);
ALTER TABLE purchase ADD CONSTRAINT pk_purchase PRIMARY KEY (id);
ALTER TABLE purchase ADD CONSTRAINT fk_purchase_person FOREIGN KEY (person_id) REFERENCES person;
